function twosum(nums, target){
    let map = {};
    for(let i=0; i<nums.length; i++){
        if(!map[nums[i]]){
            map[nums[i]]=i;
        }
    }
    for(let i=0; i<nums.length; i++){
        let temp = target - nums[i];
        if(map[temp]&&map[temp]!==i){
            return [i, map[temp]];
        }
    }
    return false;

}

